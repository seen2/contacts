interface Contact {
  firstName: string;
  lastName: string;
  phone: number;
}

const createPhone = (): number => {
  return Math.floor(Math.random() * 10 ** 10);
};

const addKey = (
  contact: Contact,
  key: number
): { firstName: string; lastName: string; phone: number; key: number } => ({
  ...contact,
  key: key,
});

const createContact = (): Contact => {
  const phone: number = createPhone();
  return {
    firstName: firstNames[phone % 10],
    lastName: lastNames[phone % 10],
    phone,
  };
};

const getContacts = (
  n: number
): { firstName: string; lastName: string; phone: number; key: number }[] => {
  let contacts = Array.from({ length: n }, createContact);

  return contacts.map(addKey);
};
export default getContacts;
